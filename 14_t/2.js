// Функція для оцінки арифметичного виразу
function calculator() {
  // Запитуємо арифметичний вираз
  let expression = prompt("Введіть арифметичний вираз:");

  // Оцінюємо вираз за допомогою eval
  let result = eval(expression);

  // Виводимо результат
  alert("Результат: " + result);
}

// Викликаємо функцію калькулятора
calculator();
